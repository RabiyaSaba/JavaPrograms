// wap a program to print star patter square
public class StarPatternSquare {
	
	public static void main(String args[])
	{
		// this will be our length
		int n =5;
		
		for (int i =0;i<n;i++)
		{
			for(int j =0;j<n;j++)
			{
				System.out.print(" *");
			}
			System.out.println("");
		}
		
	}

}
